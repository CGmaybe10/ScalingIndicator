package com.jiang.android.indicator.model;

import java.io.Serializable;

/**
 * Created by jiang on 16/2/26.
 */
public class PageModel implements Serializable {
    private CharSequence name;
    private int count;


    public PageModel() {
    }



    public PageModel(String name, int count) {
        this.name = name;
        this.count = count;
    }

    public CharSequence getName() {
        return name;
    }

    public void setName(CharSequence name) {
        this.name = name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
